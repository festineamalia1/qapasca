<h1>About QA Fisipol</h1>
<p>
Aplikasi Quality Assurance merupakan aplikasi yang dibuat untuk membantu penilaian dan evaluasi 
dalam akreditasi di Fakultas Ilmu Sosial dan Ilmu Politik.
Tujuan dari Quality Assurance adalah meningkatkan reputasi dan akreditasi internasional 
di bidang Pendidikan, Riset dan Pengabdian kepada Masyarakat di sebuah institusi pendidikan seperti UGM.
Landasan yang digunakan dalam Quality Assurance Fakultas Ilmu Sosial dan Ilmu Politik 
menggunakan Akreditasi BAN-PT yang terdiri dari 7 Standar penilaian.
</p>